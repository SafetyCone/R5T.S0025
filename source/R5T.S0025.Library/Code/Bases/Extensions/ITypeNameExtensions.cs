using System;

using R5T.T0034;


namespace System
{
    public static class ITypeNameExtensions
    {
        public static string IExtensionMethodBaseFunctionalityExtensions(this ITypeName _)
        {
            var output = _.GetExtensionsOfTypeNameTypeName(
                _.IExtensionMethodBaseFunctionality());

            return output;
        }
    }
}
